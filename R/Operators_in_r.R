#---------------------------------------------Operator in R-------------------------------------

# There are four main types of operators:
  
# 1.Arithmetic

# 2.Relational 

# 3. Logical

# 4. Assignment 

#Arithmetic Operator
a<-9
b<-7
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%%b)
print(a%/%b)
print(a^b)


# Relational Operator
a<-2
b<-5
print(a<b)
print(a>b)
print(a<=b)
print(a>=b)
print(a==b)
print(a!=b)

#Logical operators
